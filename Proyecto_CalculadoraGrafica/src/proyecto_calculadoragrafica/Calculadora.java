public class Calculadora {
    
    double numero1;
    double numero2;
    double resultado;
    
    public double sumar(){
        resultado=numero1+numero2;       
        return resultado;
    }
     public double restar(){
        resultado=numero1-numero2;       
        return resultado;
    }
      public double multiplicar(){
        resultado=numero1*numero2;       
        return resultado;
    }
       public double dividir(){
        resultado=numero1/numero2;       
        return resultado;
    }
        
    

    
}
